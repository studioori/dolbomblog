// 랜덤하게 n개의 반응을 추출하는 함수
export const getRandomReactions = (category: ActivityCategory, count: number = 7): string[] => {
  const reactions = [...CATEGORY_REACTIONS[category]];
  
  // Fisher-Yates 셔플 알고리즘
  for (let i = reactions.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [reactions[i], reactions[j]] = [reactions[j], reactions[i]];
  }
  
  return reactions.slice(0, count);
};
